const LINE_NUMBER = 50;
const MAGIC_RATIO = 0.549;
const LETTER_NUMBER = Math.ceil(window.innerWidth / (window.innerHeight / LINE_NUMBER * MAGIC_RATIO));
const between = (e, l, r) => l <= e && e <= r;


let screen_array = [];
let rain_buffer = [];
let rain_buffer_copy = [];


/**
 * Initializes the screen array and rain buffer
 * 
 * @returns Nothing
 */
function screenInit () {
    screen_array = [];
    rain_buffer = [];
    rain_buffer_copy = [];

    // Initialize Poytech things

    let top = Math.floor((LINE_NUMBER - poly_array.length) / 2);
    let bottom = top + poly_array.length;
    let left = Math.floor((LETTER_NUMBER - poly_array[0].length) / 2);
    let right = left + poly_array[0].length;

    for (let i = 0 ; i < LINE_NUMBER ; i++) {
        screen_array.push([]);
        rain_buffer.push([]);
        rain_buffer_copy.push([]);
        for (let j = 0 ; j < LETTER_NUMBER ; j++) {
            // Each array element is an object representation of a char
            // Allowing to keep track of color and if it's in polytech or not
            // color acts as a countdown from 100 to 0
            // When 0 is on color, we "delete" the object by setting char to " "
            screen_array[i].push({
                char: " ",
                color: 50,
                isPolytech: (between(i, top, bottom - 1) && between(j, left, right - 1) && poly_array[i - top][j - left]) ? 180 : 110
            });

            // For the rain buffer, if a true is on the buffer, we move it one
            // down and put a false
            // Example :
            // [[false, true ],  ->  [[false, false]
            //  [true,  false]]  ->   [false, true ]]
            rain_buffer[i].push(false);
            rain_buffer_copy[i].push(false);
        }
    }
}


/**
 * Blits the screen array
 * 
 * @returns Nothing
 */
function blitArray () {
    let text = "";
    for (let i = 0 ; i < LINE_NUMBER ; i++) {
        for (let j = 0 ; j < LETTER_NUMBER ; j++) {
            // Use all of what's described above to make a perfect screen
            if (screen_array[i][j].char !== " ") {
                text += "<span style='color: hsl(" + screen_array[i][j].isPolytech +
                        ", 100%, " + screen_array[i][j].color + "%);'>" + screen_array[i][j].char + "</span>";
            } else {
                text += " ";
            }
        }
        text += "\n";
    }
    document.getElementById("matrix").innerHTML = text;
}


/**
 * Makes up all the matrix rain effect, optimized
 */
function makeItRain () {
    for (let i = 0 ; i < LINE_NUMBER ; i++) {
        for (let j = 0 ; j < LETTER_NUMBER ; j++) {
            // Apply fading
            if (screen_array[i][j].char !== " ") {
                screen_array[i][j].color -= 5;
            }

            // Apply destruction
            if (screen_array[i][j].color === 0) {
                screen_array[i][j].char = " ";
            }

            // Apply creation
            if (rain_buffer[i][j]) {
                screen_array[i][j].color = 100;
                screen_array[i][j].char = String.fromCharCode(Math.floor(Math.random() * 93) + 33);

                // Falling time
                if (i < LINE_NUMBER - 1) {
                    rain_buffer_copy[i + 1][j] = true;
                }
            }
        }
    }

    // Copy back
    for (let i = 0 ; i < LINE_NUMBER ; i++) {
        for (let j = 0 ; j < LETTER_NUMBER ; j++) {
            rain_buffer[i][j] = rain_buffer_copy[i][j];
            rain_buffer_copy[i][j] = false;
        }
    }

    // Create more falling
    for (let i = 0 ; i < LETTER_NUMBER ; i++) {
        if (Math.random() < 0.015) {
            rain_buffer[0][i] = true;
        }
    }

    blitArray();
}

screenInit();
blitArray();

setInterval(makeItRain, 100);